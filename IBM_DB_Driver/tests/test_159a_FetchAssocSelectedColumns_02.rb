class TestIbmDb < Test::Unit::TestCase

  def test_159a_FetchAssocSelectedColumns_02
    assert_expect do
      conn = IBM_DB::connect db,user,password

      server = IBM_DB::server_info( conn )
      if (server.DBMS_NAME[0,3] == 'IDS')
        op = {IBM_DB::ATTR_CASE => IBM_DB::CASE_UPPER}
        IBM_DB::set_option conn, op, 0
      end

      result = IBM_DB::exec conn, "select prstdate,prendate from project"
      i=1
      
      while (row = IBM_DB::fetch_assoc(result))
        printf("%3d %10s %10s\n",i, row['PRSTDATE'], row['PRENDATE'])
        i+=1
      end
    end
  end

end

